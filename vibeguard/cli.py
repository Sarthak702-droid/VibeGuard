@app.command("all")
def run_all(
    goal_arg: str = typer.Argument(None, help="Goal for the full VibeGuard workflow."),
    goal: str = typer.Option(None, "--goal", "-g", help="Goal for the full VibeGuard workflow."),
    project: Path = typer.Option(Path("."), "--project", "-p", help="Project directory."),
    max_tokens: int = typer.Option(8000, "--max-tokens", "-t", help="Maximum estimated token budget."),
    no_banner: bool = typer.Option(False, "--no-banner", help="Disable the startup banner."),
) -> None:
    """Run the complete MVP workflow."""
    project = safe_path(project)
    if no_banner:
        os.environ["VIBEGUARD_NO_BANNER"] = "1"
    print_banner(console, compact=False, project=project)
    resolved_goal = goal or goal_arg
    if not resolved_goal:
        console.print("ERROR: Missing option '--goal' / '-g' or positional argument.")
        raise typer.Exit(1)
    console.print("Running VibeGuard workflow...")
    console.print("")
    if not project.exists():
        console.print(f"ERROR: Project path '{display_path(project)}' does not exist.")
        raise typer.Exit(1)

    try:
        ensure_vibeguard_dir(project)
    except PermissionError:
        console.print("ERROR: Permission denied. Cannot create .vibeguard folder.")
        raise typer.Exit(1)
    except Exception as e:
        console.print(f"ERROR: Cannot create .vibeguard folder. Reason: {e}")
        raise typer.Exit(1)

    scan_result = scan_project(project)
    if scan_result.detection.package_manager in ("yarn", "pnpm", "bun"):
        console.print(f"WARNING: Detected {scan_result.detection.package_manager} project. Automatic verification currently supports npm in MVP.")
    _write_scan_cache(project, scan_result)
    console.print("OK scan completed")

    context_pack = pack_files(project, scan_result, resolved_goal, max_tokens)
    write_text(project, "context.md", build_context(scan_result, resolved_goal, context_pack))
    console.print("OK context generated")

    write_text(project, "task.md", build_plan(scan_result, resolved_goal))
    console.print("OK plan generated")

    prompt_pack = pack_files(project, scan_result, resolved_goal, max_tokens)
    write_text(project, "prompt.md", build_prompt(scan_result, resolved_goal, prompt_pack))
    console.print("OK prompt generated")

    pack_result = pack_files(project, scan_result, resolved_goal, max_tokens)
    write_text(project, "pack.md", build_pack(scan_result, resolved_goal, pack_result, max_tokens))
    console.print("OK pack generated")

    verification = verify_project(project, scan_result.detection)
    write_text(project, "reports/verification_report.md", render_verification_report(verification, scan_result.detection.primary_type, scan_result.detection.package_manager or "Unknown"))
    if any(c.status == "Failed" for c in verification.checks):
        console.print("WARNING verify completed with failed checks")
    elif any(c.status == "Skipped" for c in verification.checks):
        console.print("WARNING verify completed with skipped checks")
    else:
        console.print("OK verification completed")

    diff = analyze_diff(project)
    if diff.status != "ok":
        if diff.status == "git_missing":
            console.print("WARNING Git not available. Skipping diff analysis.")
        elif diff.status == "not_git_repo":
            console.print("WARNING No Git repository detected. Skipping diff analysis.")
        elif diff.status == "no_commits":
            console.print("WARNING Git repository has no baseline commit. Skipping diff analysis.")
    else:
        write_text(project, "reports/diff_report.md", render_diff_report(diff))
        console.print("OK diff report generated")

        risk_report = analyze_risks(diff)
        write_text(project, "reports/risk_report.md", render_risk_report(risk_report))
        if any(r.severity in ("MEDIUM", "HIGH") for r in risk_report.risks):
            console.print("WARNING risk report generated with medium/high findings")
        else:
            console.print("OK risk report generated")

        risk_lines = [f"{risk.severity} {risk.path + ': ' if risk.path else ''}{risk.message}" for risk in risk_report.risks]
        write_text(project, "reports/next_prompt.md", build_next_prompt(risk_lines))
        console.print("OK next prompt generated")

    console.print("")
    console.print("Workflow complete.")
    console.print("Reports written to .vibeguard/reports")


@app.command("diff-explain")
def diff_explain(
    project: Path = typer.Option(Path("."), "--project", "-p", help="Project directory."),
    no_banner: bool = typer.Option(False, "--no-banner", help="Disable the startup banner."),
) -> None:
    """Explain uncommitted Git changes."""
    project = safe_path(project)
    if no_banner:
        os.environ["VIBEGUARD_NO_BANNER"] = "1"
    print_banner(console, compact=True, project=project)
    diff = analyze_diff(project)
    if diff.status == "git_missing":
        console.print("Git not available. Skipping diff analysis.")
        return
    elif diff.status == "not_git_repo":
        console.print("No Git repository detected. Run `git init` to enable diff analysis.")
        return
    elif diff.status == "no_commits":
        console.print("Git repository has no baseline commit. Create an initial commit to enable meaningful diff analysis.")
        return

    path = write_text(project, "reports/diff_report.md", render_diff_report(diff))
    if not diff.changed_files:
        console.print("No uncommitted Git diff detected.")
    else:
        console.print("Changed files:")
        for changed in diff.changed_files:
            console.print(f"- {changed.path}")
        risk_notes = _diff_risk_notes([changed.path for changed in diff.changed_files])
        if risk_notes:
            console.print("Risk:")
            for note in risk_notes:
                console.print(f"- {note}")
    console.print(f"Report written: {display_path(path)}")


