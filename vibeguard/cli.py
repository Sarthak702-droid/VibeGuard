@app.command("next-prompt")
def next_prompt(
    project: Path = typer.Option(Path("."), "--project", "-p", help="Project directory."),
    no_banner: bool = typer.Option(False, "--no-banner", help="Disable the startup banner."),
) -> None:
    """Generate the next best prompt after reviewing risky changes."""
    project = safe_path(project)
    if no_banner:
        os.environ["VIBEGUARD_NO_BANNER"] = "1"
    print_banner(console, compact=True, project=project)
    diff = analyze_diff(project)
    if diff.status == "git_missing":
        console.print("Git not available. Skipping diff analysis.")
        return
    elif diff.status == "not_git_repo":
        console.print("No Git repository detected. Run `git init` to enable diff analysis.")
        return
    elif diff.status == "no_commits":
        console.print("Git repository has no baseline commit. Create an initial commit to enable meaningful diff analysis.")
        return

    report = analyze_risks(diff)
    risk_lines = [f"{risk.severity} {risk.path + ': ' if risk.path else ''}{risk.message}" for risk in report.risks]
    path = write_text(project, "reports/next_prompt.md", build_next_prompt(risk_lines))
    console.print(f"Next prompt written: {display_path(path)}")


def _doctor_required(label: str, ok: bool) -> int:
    if ok:
        console.print(f"OK {label}")
        return 0
    console.print(f"FAILED {label}")
    return 1


def _doctor_warning(label: str, ok: bool) -> int:
    if ok:
        console.print(f"OK {label}")
        return 0
    console.print(f"WARNING {label}")
    return 1


def _has_git_repository(root: Path) -> bool:
    if (root / ".git").exists():
        return True
    try:
        result = subprocess.run(["git", "rev-parse", "--is-inside-work-tree"], cwd=root, text=True, capture_output=True, check=False)
    except FileNotFoundError:
        return False
    return result.returncode == 0 and result.stdout.strip() == "true"


def _diff_risk_notes(paths: list[str]) -> list[str]:
    lower_paths = [path.lower() for path in paths]
    notes: list[str] = []
    if any("auth" in path for path in lower_paths):
        notes.append("Auth-related file changed")
    if paths and not any(any(term in path for term in (".test.", ".spec.", "__tests__", "/tests/", "\\tests\\")) for path in lower_paths):
        notes.append("No test file changed")
    return notes


def _risk_reason(risk: Risk) -> str:
    lower = risk.message.lower()
    if "auth" in lower:
        return "auth-related files require manual review"
    if "no test" in lower:
        return "feature code changed without test update"
    if "secret" in lower:
        return "changed code contains possible hardcoded secret/API key/token"
    if "dependency" in lower or "lockfile" in lower:
        return "dependency changes can affect runtime and supply-chain safety"
    if "deleted" in lower:
        return "deleted files may break imports or behavior"
    if "large" in lower:
        return "large diffs are harder to review and may include unrelated edits"
    return risk.message.rstrip(".")


def _write_scan_cache(project: Path, result) -> None:
    out = ensure_vibeguard_dir(project)
    data = {
        "type": result.detection.primary_type,
        "languages": result.detection.languages,
        "frameworks": result.detection.frameworks,
        "files": result.files,
        "important_files": result.important_files,
        "ignored": result.ignored,
    }
    (out / "cache" / "scan.json").write_text(json.dumps(data, indent=2), encoding="utf-8")


